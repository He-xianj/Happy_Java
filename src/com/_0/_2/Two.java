package com._0._2;
import java.util.Scanner;
/**
 * 给定一个年份，判断这一年是不是闰年。当以下情况之一满足时，这一年是闰年：
 * 1. 年份是4的倍数而不是100的倍数；
 * 2. 年份是400的倍数。
 * 其他的年份都不是闰年。
 * 输入格式输入包含一个整数y，表示当前的年份。
 * 输出格式输出一行，如果给定的年份是闰年，则输出yes，否则输出no。 
 * */
public class Two {
	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		int y=scanner.nextInt();
		scanner.close();
		//	输入包含一个整数y，表示当前的年份。
		if(y%4==0&y%100!=0) {
			//判断这一年是不是闰年
			System.out.println("yes");
			//年份是4的倍数&不是100的倍数；
		}else {
			if(y%400==0) {
				System.out.println("yes");
				// 或者年份是400的倍数
			 }else {
				 System.out.println("no");
				 //其他的年份都不是闰年,输出no。
			 }	
		}
	}
}

